// --- Swift type mapper ---

const SWIFT_BASE_TYPES: Record<string, string> = {
  string: "String",
  integer: "Int",
  number: "Double",
  boolean: "Bool",
  unknown: "Any",
};

export const swiftTypeMapper: TypeMapper = {
  baseType(type: string): string {
    return SWIFT_BASE_TYPES[type] ?? "Any";
  },
  enumLiteral(value: string | number | boolean): string {
    if (typeof value === "string") return `"${value}"`;
    return String(value);
  },
  enumUnion(_values: (string | number | boolean)[]): string {
    // In Swift, enums are named types — this returns a placeholder.
    // Actual enum rendering is handled separately.
    return "String";
  },
  arrayOf(innerType: string): string {
    return `[${innerType}]`;
  },
  optional(type: string): string {
    return `${type}?`;
  },
};

// --- Helpers ---

/** Convert a snake_case field name to camelCase for Swift properties */
function toCamelCase(str: string): string {
  return str.replace(/[_-](\w)/g, (_, c) => c.toUpperCase());
}

/** Generate a Swift enum name from a field name, e.g. "auth_method" → "AuthMethod" */
function enumName(fieldName: string): string {
  return toPascalCase(fieldName);
}

